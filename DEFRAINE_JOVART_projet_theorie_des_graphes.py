class Graph():
    def __init__(self, vertices):
        """

        :param vertices: numéro du sommet, initialise un sommet
        """
        self.graph = defaultdict(list)
        self.V = vertices

    def addEdge(self, u, v):
        """

        :param u: sommet de départ
        :param v: sommet d'arrivée
        :return: crée un arc entre u et v
        """
        self.graph[u].append(v)

    def isCyclicUtil(self, v, visited, recStack):
        # Mark current node as visited and
        # adds to recursion stack
        visited[v] = True
        recStack[v] = True

        # Recur for all neighbours
        # if any neighbour is visited and in
        # recStack then graph is cyclic
        for neighbour in self.graph[v]:
            if not visited[neighbour]:
                if self.isCyclicUtil(neighbour, visited, recStack):
                    return True
            elif recStack[neighbour]:
                return True

        # The node needs to be poped from
        # recursion stack before function ends
        recStack[v] = False
        return False

    # Returns true if graph is cyclic else false
    def isCyclic(self):
        """

        :return: True si le graphe est cyclique sinon False
        """
        visited = [False] * self.V
        recStack = [False] * self.V
        for node in range(self.V):
            if visited[node] == False:
                if self.isCyclicUtil(node, visited, recStack):
                    return True
        return False


class Node:
    def __init__(self, nom):
        self.in_nodes = []
        self.out_nodes = []
        self.nom = nom

    def __str__(self):
        return self.nom

    def __repr__(self):
        return str(self)


def sommet(c, x, y):
    """

    :param c: nom ou numéro du sommet
    :param x: position x du sommet
    :param y: position y du sommet
    :return: dessine un sommet à la position x,y
    """

    tu.color("black")
    tu.seth(0)
    tu.up()
    tu.setpos(x, y)
    tu.down()
    tu.circle(7)
    tu.write(c, False, align="center", font=("Arial", 7, "normal"))


def arc(x, y, z, s, color):
    """

    :param x: position x de départ du curseur
    :param y: position y de départ du curseur
    :param z: position x d'arrivée du curseur
    :param s: sens de l'arc
    :param color: couleur de l'arc
    :return: dessine l'arc allant de x à z
    """
    tu.seth(0)
    r = 0.5 * abs(x - z)
    tu.color(color)
    tu.up()
    tu.left(90)
    tu.setpos(x, y)
    tu.down()
    if s:
        tu.up()
        tu.setpos(x, y + 14)
        tu.down()
        tu.circle(-r, 180)
    if not s:
        tu.up()
        tu.setpos(x, y)
        tu.down()
        tu.circle(-r, -180)


def graph(M):
    """

    :param M: liste de liste (matrice d'incidence d'un graphe)
    :return: dessine le graphe de matrice d'incidence M
    """
    for i in range(len(M)):
        sommet(i + 1, -700 + (15 * i), -150)
    for i in range(len(M)):
        for j in range(len(M)):
            if (M[i][j]) == 1:
                if j < i:
                    arc(-700 + (15 * j), -150, -700 + (15 * i), 0, "green")
                else:
                    arc(-700 + (15 * i), -150, -700 + (15 * j), 1, "red")


def checkCycle(g):
    """

    :param g: matrice d'incidence d'un graphe
    :return bool: True si le graphe contient (avec un message d'erreur) un cycle sinon False
    """
    G = Graph(len(g))
    for i in range(len(g)):
        for j in range(len(g)):
            if g[i][j]:
                G.addEdge(i, j)
    if G.isCyclic() == 1:
        print("Le graphe d'entrée comporte un cycle et/ou une symétrie sur sa diagonale, entrez un graphe orienté et "
              "acyclique s'il vous plait.")
        return True
    else:
        return False


def planning(S):
    """

    :param S: vecteur tp des temps
    :return: affiche le planning au plus vite et le planning séquentiel sous la forme demandé
    """
    cnt = 0
    print("Planning au plus vite")
    for clef in S:
        print(clef, ':', S[clef])

    print("Planning séquentiel")
    for clef in S:
        for element in S[clef]:
            cnt += 1
            print(cnt, ':', element)


def plan(M):
    """

    :param M: matrice d'incidence d'un graphe
    :return: vecteur tp des temps
    """
    nodes = [Node(str(i)) for i in range(len(M))]
    for i, ligne in enumerate(M):
        ma_node = nodes[i]
        for j, case in enumerate(ligne):
            if case:
                la_node = nodes[j]
                ma_node.out_nodes.append(la_node)
                la_node.in_nodes.append(ma_node)

    jours = {}
    nodes_restantes = nodes[:]
    jour = 0
    while len(nodes_restantes):
        jour = jour + 1
        nodes_to_remove = []
        for node in nodes_restantes:
            node = next(n for n in nodes if n.nom == node.nom)
            if len(node.in_nodes) == 0:
                nodes_to_remove.append(node)
                if jour in jours:
                    jours[jour].append(node)
                else:
                    jours[jour] = [node]
        for node in nodes_to_remove:
            nodes_restantes.remove(node)
            for node_ in node.out_nodes:
                node_.in_nodes.remove(node)
    return planning(jours)


